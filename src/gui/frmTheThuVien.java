    // Lấy mã độc giả
    public String layMaDG() throws Exception {
        STT = 0;
        ResultSet rs = bllDG.layDS();
        rs.last();
        // JOptionPane.showMessageDialog(null, rs.getRow());
        int row = rs.getRow();
        if (row == 0) {
            STT = 1;
        } else {
            rs.beforeFirst();
            while (rs.next()) {
                STT = Integer.parseInt(rs.getObject(1).toString().substring(2)) + 1;
            }
        }
        String ma = String.format("%03d", STT);
        return "DG" + ma;
    }

