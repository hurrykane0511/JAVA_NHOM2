    private void hienThi() {
        DefaultTableModel model = (DefaultTableModel) tblDanhMuc.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Mã danh mục");
        model.addColumn("Tên danh mục");
        tblDanhMuc.setModel(model);
    }

    private void hienThiDM() throws Exception {
        DefaultTableModel model = (DefaultTableModel) tblDanhMuc.getModel();
        model.setRowCount(0);
        ResultSet rs = bll.layDM();
        while (rs.next()) {
            Object[] col = new Object[2];
            for (int i = 1; i <= 2; i++) {
                col[i - 1] = rs.getObject(i);
            }
            model.addRow(col);
        }
        tblDanhMuc.setModel(model);
    }

    public void layMa() throws Exception {
        ResultSet rs = bll.layDM();
        rs.last();
        // JOptionPane.showMessageDialog(null, rs.getRow());
        int row = rs.getRow();
        if (row == 0) {
            STT = 1;
        } else {
            rs.beforeFirst();
            while (rs.next()) {
                STT = Integer.parseInt(rs.getObject(1).toString().substring(2)) + 1;
            }
        }
        String ma = String.format("%02d", STT);
        txtMaDM.setText("DM" + ma);
        txtTenDM.setText("");
        txtTenDM.requestFocus();
    }


