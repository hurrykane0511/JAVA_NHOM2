    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maTK = txtMaTK.getText();
        String tenDN = txtTenDN.getText();
        String mk = txtMK.getText();
        String loaiTK = cboLoaiTK.getSelectedItem().toString();
        String maNV = txtMaNV.getText();
        ET_TaiKhoan et;
        BLL_NhanVien bllNV = null;
        if (tenDN.compareTo("") == 0 || mk.compareTo("") == 0 || loaiTK.compareTo("") == 0 || maNV.compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                if (bll.checkTT(maTK)) {
                    JOptionPane.showMessageDialog(null, "Đã tồn tại tài khoản này");
                } else {
                    bllNV = new BLL_NhanVien();
                    if (!bllNV.checkTT(maNV)) {
                        JOptionPane.showMessageDialog(null, "Không tồn tại nhân viên này");
                    } else {
                        et = new ET_TaiKhoan(maTK, tenDN, mk, loaiTK, maNV);
                        bll.themTK(et);
                        layMa();
                        hienThiTK();
                    }
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

